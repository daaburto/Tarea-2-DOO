package org.example;

public class Empleado {
    private String id;
    private String apellidos;
    private String nombre;
    private String correo;

    public Empleado{

    }

    public String getId() {
        return id;
    }

    public String getApellidos() {
        return apellidos;
    }

    public String getNombre() {
        return nombre;
    }

    public String getCorreo() {
        return correo;
    }
}